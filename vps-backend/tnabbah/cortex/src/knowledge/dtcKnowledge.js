function normalizeDtc(code) {
  return String(code || "").trim().toUpperCase();
}

function getDtcFamily(code) {
  const normalized = normalizeDtc(code);
  const first = normalized[0];

  return (
    DTC_SYSTEMS[first] || {
      title: "نظام غير معروف",
      simpleName: "كود غير مصنف",
    }
  );
}

function guessSeverity(code) {
  const normalized = normalizeDtc(code);

  if (normalized.startsWith("B00")) return "critical";
  if (normalized.startsWith("U01")) return "warning";
  if (normalized.startsWith("P03")) return "critical";
  if (normalized.startsWith("P02")) return "critical";
  if (normalized.startsWith("P07")) return "warning";
  if (normalized.startsWith("C00")) return "warning";

  return "warning";
}

function getDtcKnowledge(code) {
  const normalized = normalizeDtc(code);
  const known = DTC_KNOWLEDGE[normalized];
  const family = getDtcFamily(normalized);

  if (known) {
    return {
      code: normalized,
      system: family.title,
      systemMeaning: family.simpleName,
      ...known,
    };
  }

  return {
    code: normalized,
    title: "كود عطل من السيارة",
    simpleName: "السيارة سجلت عطل يحتاج فحص",
    severity: guessSeverity(normalized),
    system: family.title,
    systemMeaning: family.simpleName,
    userMeaning:
      "السيارة أرسلت كود عطل، لكن ما عندنا له شرح مفصل حتى الآن. هذا ما يعني إنه غير مهم، فقط يحتاج تفسير أعمق حسب نوع السيارة.",
    possibleCauses: [
      "قد يكون حساس",
      "قد يكون سلك أو فيشة",
      "قد يكون جزء ميكانيكي",
      "قد يكون كود خاص بالشركة المصنعة",
    ],
    simpleTips: [
      "لا تسمحين لأحد يغير قطعة غالية بناءً على الكود فقط.",
      "الكود بداية التشخيص، مو الحكم النهائي.",
      "الأفضل ربطه مع الأعراض والقراءات الحية.",
    ],
  };
}

function explainDtcList(codes = []) {
  return codes.map((code) => getDtcKnowledge(code));
}

